    private void btnAddNewStorageJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewStorageJobActionPerformed
        // TODO add your handling code here:
        
        if(txtUserID.getText().isEmpty()){
            showErrorAlert();
            txtUserID.requestFocus();
            
        }else if(txtProductName.getText().isEmpty()){
            showErrorAlert();
            txtProductName.requestFocus();
          
        }else if(txtSpace.getText().isEmpty()){
            showErrorAlert();
            txtSpace.requestFocus();
            
        }else if(txtDuration.getText().isEmpty()){
            showErrorAlert();
            txtDuration.requestFocus();
            
        }else if(txtDate.getText().isEmpty()){
            showErrorAlert();
            txtDate.requestFocus();
            
        }else if(Profile.getSelectedItem().toString().isEmpty()){
            showErrorAlert();
            Profile.requestFocus();
        }else{
            
            
            try {
                
                String userID = txtUserID.getText();
                String proname = txtProductName.getText();
                String space = txtSpace.getText();
                String duration = txtDuration.getText();
                String date = txtDate.getText();
                String profile = (String)Profile.getSelectedItem();
                
                
                
                
                Connection connection = DBConnection.getInstance().getConnection();
                PreparedStatement prepareStatement = connection.prepareStatement("insert into add_new_storage_job values (?,?,?,?,?,?)");
                
                prepareStatement.setString(1, userID);
                prepareStatement.setString(2, proname);
                prepareStatement.setString(3, space);
                prepareStatement.setString(4, duration);
                prepareStatement.setString(5, date);
                prepareStatement.setString(6, profile);
                
                
                prepareStatement.executeUpdate();
                
                
                JOptionPane.showMessageDialog(null, "Successfully Added");
                
                txtUserID.setText("");
                txtProductName.setText("");
                txtSpace.setText("");
                txtDuration.setText("");
                txtDate.setText("");
                txtUsername.setText("");
                txtEmail.setText("");
                
                txtUserID.requestFocus();
                      
               
                
            } catch (SQLException ex) {
                Logger.getLogger(AddNewStoreJobForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"DB Faild"+ex.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
            }
        
        
        
        }
    }//GEN-LAST:event_btnAddNewStorageJobActionPerformed

