    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        try {
            // TODO add your handling code here:
            
            
           Connection connection =  DBConnection.getInstance().getConnection();
           PreparedStatement prepareStatement = connection.prepareStatement("SELECT username,email FROM register_user WHERE user_ID = ?");
            
            
            String userid = txtUserID.getText();
            prepareStatement.setString(1, userid);
            

            ResultSet rs = prepareStatement.executeQuery();

            if(rs.next()==false){
                JOptionPane.showMessageDialog(null,"Recode Not Found","ERROR",JOptionPane.ERROR_MESSAGE);
                txtUserID.setText("");
                txtUserID.requestFocus();
            }else
            {
                txtUsername.setText(rs.getString("username"));
                txtEmail.setText(rs.getString("email"));
                txtProductName.requestFocus();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"DB Connection Error"+ex.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);

        }
       
        
        
    }//GEN-LAST:event_btnFindActionPerformed

